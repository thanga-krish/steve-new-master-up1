package de.rwth.idsg.steve.myconfig;

import org.joda.time.DateTime;
import org.jooq.Converter;

import java.sql.Timestamp;

public class JodaDateTimeConverter implements Converter<Timestamp, DateTime> {

    @Override
    public DateTime from(Timestamp t) {
        return t == null ? null : new DateTime(t.getTime());
    }

    @Override
    public Timestamp to(DateTime dt) {
        return dt == null ? null : new Timestamp(dt.getMillis());
    }

    @Override
    public Class<Timestamp> fromType() {
        return Timestamp.class;
    }

    @Override
    public Class<DateTime> toType() {
        return DateTime.class;
    }
}
