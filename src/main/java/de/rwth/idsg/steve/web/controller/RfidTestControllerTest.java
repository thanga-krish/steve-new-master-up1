package de.rwth.idsg.steve.web.controller;

import de.rwth.idsg.steve.service.RfidTagService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/test")
public class RfidTestControllerTest {

    @Autowired
    private RfidTagService rfidTagService;

    @PostMapping("/store-rfid")
    public String storeTestRfid(@RequestParam String rfid, @RequestParam String userId) {
        rfidTagService.storeRfidTag(rfid, userId);
        return "RFID Stored!";
    }
}
